
public class Pattern2 {
	
	public static void main(String[] args) {
		
		
		 for(int i=7;i>=1;i--){
				
				for(int j=1;j<=i;j++){
					
					System.out.print(" ");
					
				}
				System.out.println("*");
				System.out.println();
				
			}
		 
			for(int m=1;m<8;m++){
				
				for(int n=1;n<=m;n++){
					
					System.out.print(n+" ");
					
				}
				System.out.println();
				
			}
	}

}
