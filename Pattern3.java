
public class Pattern3 {

	public static void main(String[] args) {
		
		for(int i=1;i<8;i++){
			
			for(int j=1;j<=i;j++){
				System.out.print(j+" ");
			}
			System.out.println();
		}
		
		for(int m=6;m>=1;m--){
			
			for(int n=1;n<=m;n++){
				
				System.out.print(n+" ");
			}
			System.out.println();
		}
		
		
	}
}
